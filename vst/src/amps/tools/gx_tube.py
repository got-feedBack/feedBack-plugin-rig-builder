#!/usr/bin/env python3
# Our own PURE tube transfer tables (Guitarix-style architecture, NOT its GPL code).
# Physics is public: Koren triode/pentode model (Norman Koren), datasheet SPICE
# constants, grid-conduction Igk=exp(7.75*Vgk-10.3), and the Yeh/Karjalainen
# load-line circuit solve (academic, DAFx). The table is the PURE plate transfer
# Ftube(Vgrid)->Vplate with NO cathode -- the cathode is modeled OUTSIDE in the
# stage's feedback loop (Rk, Ck, Vk0), exactly like a real cathode-biased stage.
import numpy as np

def Igk(Vgk):                                   # grid conduction current (A)
    return np.exp(np.clip(7.75*Vgk - 10.3, -80.0, 50.0))

def Ipk_triode(Vgk, Vpk, mu, ex, kg1, kp, kvb):
    a  = kp*(1.0/mu + Vgk/np.sqrt(kvb + Vpk*Vpk))
    E1 = (Vpk/kp)*np.log1p(np.exp(np.clip(a, -60.0, 60.0)))
    return np.where(E1 > 0.0, 2.0*np.power(np.maximum(E1, 0.0), ex)/kg1, 0.0)

def Ipk_pentode(Vgk, Vpk, mu, ex, kg1, kp, kvb, kg2):
    # screen tied for class-A: Koren pentode plate current
    E1 = (Vpk/kp)*np.log1p(np.exp(np.clip(kp*(1.0/mu + Vgk/Vpk), -60.0, 60.0)))
    G1 = 2.0*np.power(np.maximum(E1, 0.0), ex)/kg1*(E1 > 0.0)*np.arctan(Vpk/kvb)
    return G1/kg2 * kg2   # (kg2 folds into scale below; keep shape)

def solve_Vgk(Vi, Ri):
    # KVL at grid: Vi = Vgk + Ri*Igk(Vgk)  -> Newton for Vgk
    Vgk = np.minimum(Vi, 0.0).astype(float)
    for _ in range(80):
        f = Vi - Vgk - Ri*Igk(Vgk)
        d = -1.0 - Ri*7.75*Igk(Vgk)
        Vgk = Vgk - f/np.where(np.abs(d) < 1e-12, -1e-12, d)
    return Vgk

def solve_Vpk(Vgk, Vsupply, Rp, ipk, args):
    # load line: Vsupply = Vpk + Rp*Ipk(Vgk,Vpk)  -> Newton for Vpk
    Vpk = np.full_like(np.asarray(Vgk, float), Vsupply*0.5)
    h = 1e-2
    for _ in range(120):
        I0 = ipk(Vgk, Vpk, *args)
        f  = Vpk + Rp*I0 - Vsupply
        d  = 1.0 + Rp*(ipk(Vgk, Vpk + h, *args) - I0)/h
        step = f/np.where(np.abs(d) < 1e-9, 1e-9, d)
        Vpk = np.clip(Vpk - np.clip(step, -60.0, 60.0), 0.5, Vsupply)
    return Vpk

def build_triode(name, mu, ex, kg1, kp, kvb, Vsupply, Rp, Ri_list, Vmin, Vmax, N=2001):
    Vi = np.linspace(Vmin, Vmax, N)
    tabs, ranode = [], []
    for Ri in Ri_list:
        Vgk = solve_Vgk(Vi, Ri)
        Vp  = solve_Vpk(Vgk, Vsupply, Rp, Ipk_triode, (mu, ex, kg1, kp, kvb))
        # dynamic plate resistance Ranode = dVp/dIp
        h = (Vmax - Vmin)/(N-1)
        Vgk2 = solve_Vgk(Vi + h, Ri)
        Vp2  = solve_Vpk(Vgk2, Vsupply, Rp, Ipk_triode, (mu, ex, kg1, kp, kvb))
        Ip1 = Ipk_triode(Vgk,  Vp,  mu, ex, kg1, kp, kvb)
        Ip2 = Ipk_triode(Vgk2, Vp2, mu, ex, kg1, kp, kvb)
        ra  = np.where(np.abs(Ip2-Ip1) < 1e-12, 1e5, (Vp2-Vp)/(Ip2-Ip1))
        tabs.append((Ri, Vi, Vp)); ranode.append((Ri, Vi, np.abs(ra)))
    return Vi, tabs, ranode

def build_pentode_stage(name, mu, ex, kg1, kg2, kp, kvb, Vsupply, Rp, Ri_list, Vmin, Vmax, N=2001):
    Vi = np.linspace(Vmin, Vmax, N)
    tabs, ranode = [], []
    for Ri in Ri_list:
        Vgk = solve_Vgk(Vi, Ri)
        Vp  = solve_Vpk(Vgk, Vsupply, Rp, Ipk_pentode, (mu, ex, kg1, kp, kvb, kg2))
        h = (Vmax - Vmin)/(N-1)
        Vgk2 = solve_Vgk(Vi + h, Ri)
        Vp2  = solve_Vpk(Vgk2, Vsupply, Rp, Ipk_pentode, (mu, ex, kg1, kp, kvb, kg2))
        Ip1 = Ipk_pentode(Vgk,  Vp,  mu, ex, kg1, kp, kvb, kg2)
        Ip2 = Ipk_pentode(Vgk2, Vp2, mu, ex, kg1, kp, kvb, kg2)
        ra  = np.where(np.abs(Ip2-Ip1) < 1e-12, 1e6, (Vp2-Vp)/(Ip2-Ip1))
        tabs.append((Ri, Vi, Vp)); ranode.append((Ri, Vi, np.abs(ra)))
    return Vi, tabs, ranode

def emit(path, prefix, Vmin, Vmax, N, tabs, ranode=None):
    with open(path, 'w') as o:
        o.write('#pragma once\n// Generated by /tmp/gx_tube.py -- OUR pure Koren/Yeh tube transfer.\n')
        o.write('// Pure plate transfer Vgrid->Vplate (volts); cathode is external (stage loop).\n')
        o.write('namespace rbtube {\n')
        o.write('static constexpr int %s_N = %d;\n' % (prefix, N))
        o.write('static constexpr float %s_VMIN = %.6ff;\n' % (prefix, Vmin))
        o.write('static constexpr float %s_VMAX = %.6ff;\n' % (prefix, Vmax))
        for idx, (Ri, Vi, Vp) in enumerate(tabs):
            o.write('static const float %s_FT%d[%s_N] = {\n' % (prefix, idx, prefix))
            o.write(','.join('%.6ff' % v for v in Vp))
            o.write('\n};\n')
        if ranode:
            for idx, (Ri, Vi, ra) in enumerate(ranode):
                o.write('static const float %s_RA%d[%s_N] = {\n' % (prefix, idx, prefix))
                o.write(','.join('%.3ff' % v for v in ra))
                o.write('\n};\n')
        # linear-interp lookups, idx selects Ri table (0=low Ri, 1=high Ri)
        o.write('static inline float %s_ftube(int t, float v){\n' % prefix)
        o.write('  float x=(v-%s_VMIN)*((%s_N-1)/(%s_VMAX-%s_VMIN));\n' % (prefix,prefix,prefix,prefix))
        o.write('  if(x<=0)x=0; if(x>=%s_N-1)x=%s_N-1.001f;\n' % (prefix,prefix))
        o.write('  int i=(int)x; float fr=x-i; const float*T=(t?%s_FT1:%s_FT0);\n' % (prefix,prefix))
        o.write('  return T[i]+fr*(T[i+1]-T[i]);\n}\n')
        if ranode:
            o.write('static inline float %s_ranode(int t, float v){\n' % prefix)
            o.write('  float x=(v-%s_VMIN)*((%s_N-1)/(%s_VMAX-%s_VMIN));\n' % (prefix,prefix,prefix,prefix))
            o.write('  if(x<=0)x=0; if(x>=%s_N-1)x=%s_N-1.001f;\n' % (prefix,prefix))
            o.write('  int i=(int)x; float fr=x-i; const float*T=(t?%s_RA1:%s_RA0);\n' % (prefix,prefix))
            o.write('  return T[i]+fr*(T[i+1]-T[i]);\n}\n')
        o.write('} // namespace rbtube\n')

