# ---- EL84 power pentode (AC30 class-A push-pull) ----
def Ipk_tp(Vgk,Vpk,mu,ex,kg1,kp,kvb):     # pentode (Koren, stable atan form)
    a = kp*(1.0/mu + Vgk/Vpk)
    E1 = (Vpk/kp)*np.log1p(np.exp(np.clip(a,-60.0,60.0)))
    return np.where(E1>0.0, 2.0*np.power(np.maximum(E1,0.0),ex)/kg1*np.arctan(Vpk/kvb), 0.0)

def build_pentode(name,mu,ex,kg1,kp,kvb,Vsupply,Rp,Ri,Vmin,Vmax,N=2001):
    Vi=np.linspace(Vmin,Vmax,N)
    Vgk=solve_Vgk(Vi,Ri)
    Vp=solve_Vpk(Vgk,Vsupply,Rp,Ipk_tp,(mu,ex,kg1,kp,kvb))
    return Vi,[(Ri,Vi,Vp),(Ri,Vi,Vp)]   # dup so emit's FT0/FT1 both exist

